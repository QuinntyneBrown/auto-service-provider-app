import { Injectable } from "@angular/core";
import { Http } from "@angular/http";
import { Contact } from "../models";
import { Observable } from "rxjs";

import { apiCofiguration } from "../configuration";


@Injectable()
export class ContactService {
    constructor(private _http: Http) { }

    public add(entity: Contact) {
        return this._http
            .post(`${apiCofiguration.baseUrl}/api/contact/add`, entity)
            .map(data => data.json())
            .catch(err => {
                return Observable.of(false);
            });
    }

    public get() {
        return this._http
            .get(`${apiCofiguration.baseUrl}/api/contact/get`)
            .map(data => data.json())
            .catch(err => {
                return Observable.of(false);
            });
    }

    public getById(options: { id: number }) {
        return this._http
            .get(`${apiCofiguration.baseUrl}/api/contact/getById?id=${options.id}`)
            .map(data => data.json())
            .catch(err => {
                return Observable.of(false);
            });
    }

    public remove(options: { id: number }) {
        return this._http
            .delete(`${apiCofiguration.baseUrl}/api/contact/remove?id=${options.id}`)
            .map(data => data.json())
            .catch(err => {
                return Observable.of(false);
            });
    }

    public get baseUrl() { return apiCofiguration.baseUrl; }

}
